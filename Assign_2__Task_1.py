# Check If a Number is Even Or Old  

# Define variable to store user input

Number = int(input("Please Enter a Number : "))

# Logic Implimentation Using if Else to Find Out Number is Even or Old 

if(Number % 2 == 0):
    print(f"{Number} is an Even Number")
else:
    print(f"{Number} is an Old Number")


 
